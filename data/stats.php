	// $expFormula = [
	// 	/// Numerator (Above the Line)
	// 	$a => 1,			//	1 || 1.5			Wild || Has Trainer
	// 	$t => 1,			//	1 || 1.5 || 1.7		Original Trainer || Domestic Trade || International Trade
